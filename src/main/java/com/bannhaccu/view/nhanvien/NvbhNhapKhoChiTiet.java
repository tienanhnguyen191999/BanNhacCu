    private void initTable(int id_pnk) {
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        tableSanpham.setDefaultRenderer(String.class, centerRenderer);
        tableSanpham.setDefaultRenderer(Double.class, centerRenderer);
        tableSanpham.setDefaultRenderer(Integer.class, centerRenderer);

        DefaultTableModel table = (DefaultTableModel) tableSanpham.getModel();
        ArrayList<SanPhamDuocNhapKho> arr_data = spdnkDAO.getListSanPhamNhapKho(id_pnk);

        int index = 0;
        for (SanPhamDuocNhapKho item : arr_data) {
            Object[] emptyData = {null, null, null, null, null, null};
            table.addRow(emptyData);

            DecimalFormat formatter = (DecimalFormat) NumberFormat.getInstance(Locale.US);
            formatter.applyPattern("#,###,###,###");
            tableSanpham.getModel().setValueAt(index + 1, index, 0);
            tableSanpham.getModel().setValueAt(item.getSanpham().getId(), index, 1);
            tableSanpham.getModel().setValueAt(item.getSanpham().getTen(), index, 2);
            tableSanpham.getModel().setValueAt(formatter.format(item.getSanpham().getGia()).toString(), index, 3);
            tableSanpham.getModel().setValueAt(item.getSoluong(), index++, 4);
        }

        // Set default focus when init
        tableSanpham.setRequestFocusEnabled(true);
        tableSanpham.setRowSelectionInterval(0, 0);
        tableSanpham.setColumnSelectionInterval(5, 5);
        tableSanpham.editCellAt(0, 5);
        tableSanpham.requestFocus();
        
        // Handle fill các thông tin trống
        table.addTableModelListener(new TableModelListener() {
            @Override
            public void tableChanged(TableModelEvent e) {
                try{
                    if (table.getValueAt(e.getLastRow(), e.getColumn()) != null && new Integer(table.getValueAt(e.getLastRow(), e.getColumn())+"") > new Integer(table.getValueAt(e.getLastRow(), 4)+"")) {
                        JOptionPane.showMessageDialog(null, "Số lượng thực tế <= Số lượng nhập");
                        table.setValueAt(null, e.getLastRow(), e.getColumn());
                    }
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(null, "Số lượng thực tế phải là số nguyên dương");
                    table.setValueAt(null, e.getLastRow(), e.getColumn());
                }
            }

        });
    }

