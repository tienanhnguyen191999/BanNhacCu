    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        for (int i = 0; i < tableSanpham.getModel().getRowCount(); i++) {
            if (tableSanpham.getModel().getValueAt(i, 5) == null) {
                JOptionPane.showMessageDialog(null, "Xác nhận thực nhận");
                return;
            }
        }
        try {
            // Bắt đầu transaction
            DAO.getConn().setAutoCommit(false);
            // 1. Update ischeck + Thực nhận
            phieuNhapKhoDAO.updateIsCheck(id_pnk);
            // 2. Update thuc nhan
            spdnkDAO.updateThucNhan(tableSanpham.getModel(), id_pnk);
            // 3. Cap nhat soluong sanpham
            sanPhamDAO.updateSoluong(tableSanpham.getModel());
            // Commit transaction
            DAO.getConn().commit();
            JOptionPane.showMessageDialog(null, "Nhập kho thành công");
            this.dispose();
            Arrays.asList(Window.getWindows()).forEach(e -> e.dispose());
            new NvbhNhapkho(nv).setVisible(true);
        } catch (Exception ex) {
            try {
                // Rollback transaction
                DAO.getConn().rollback();
            } catch (SQLException ex1) {
                Logger.getLogger(NvbhNhapKhoChiTiet.class.getName()).log(Level.SEVERE, null, ex1);
            }
            Logger.getLogger(NvbhNhapKhoChiTiet.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

