    private Double calculateTotal () {
        double total = 0;
        for (int i = 0; i < tableSanpham.getModel().getRowCount(); i++) {
            if (tableSanpham.getModel().getValueAt(i, 1) != null) {
                total += (Double)tableSanpham.getModel().getValueAt(i, 6);
            }
        }
        return total;
    }
    
